        // TODO add your handling code here:
         try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost/campus","root","");
            Statement st=con.createStatement();
            String q,sid,fn,ln,str,per,ph,em,apt,cn;
            cn=cmbCompany.getSelectedItem().toString();
            q="select * from student where perc>=(select min_per from company where cname='"+cn+"') order by apt_marks desc";
            ResultSet rs=st.executeQuery(q);
            DefaultTableModel df=(DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            while(rs.next())
            {
                sid=rs.getString("sid");
                fn=rs.getString("firstname");
                ln=rs.getString("lastname");
                per=rs.getString("perc");
                apt=rs.getString("apt_marks");
                str=rs.getString("stream");
                ph=rs.getString("phone");
                em=rs.getString("email");
                String data[]={sid,fn,ln,per,apt,str,ph,em};
                df.addRow(data);
            }
        }
